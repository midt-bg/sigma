#!/usr/bin/env node
// One-time generator for the canonical registry of Bulgaria's 265 municipalities (общини), keyed by
// ЕИК. Source: Wikidata — items that are an instance of (P31) "municipality of Bulgaria" (Q1906268),
// with their БУЛСТАТ/ЕИК (P8894) and the region (P131) they sit in. Output: scripts/seed-municipalities.sql
// (committed; loaded before normalize-raw/refresh-slice so type_group='община' is decided by ЕИК
// membership).
//
// Run: node scripts/build-municipalities.mjs   (output is ready to commit as-is)
//
// The municipal map of Bulgaria is near-static: the 265th municipality (Сърница) was split off in
// 2015 and nothing has changed since, so this is a regenerate-on-the-rare-occasion script, not a
// runtime feed. It throws if the source no longer yields exactly 265 municipalities
// across all 28 regions — drift then needs a human, not a silent wrong answer.

import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = resolve(root, 'scripts/seed-municipalities.sql');

const ENDPOINT = 'https://query.wikidata.org/sparql';
const QUERY = `
SELECT ?item ?name ?eik ?region WHERE {
  ?item wdt:P31 wd:Q1906268 .
  OPTIONAL { ?item wdt:P8894 ?eik . }
  OPTIONAL { ?item wdt:P131 ?reg . ?reg rdfs:label ?region FILTER(lang(?region) = "bg") }
  SERVICE wikibase:label { bd:serviceParam wikibase:language "bg". ?item rdfs:label ?name. }
}`;

// A handful of Wikidata items carry more than one P8894 statement (one correct ЕИК + a stray claim).
// Resolve each by QID to the ЕИК verified against the official БУЛСТАТ register; the generator throws
// on any *unlisted* multi-ЕИК item so a new conflict can never slip through unnoticed.
const RESOLVE_MULTI_EIK = {
  Q2220759: '000931422', // Хитрино — verified via БУЛСТАТ register; drops stray 000931205
};

const EXPECTED_COUNT = 265;
const EXPECTED_REGIONS = 28;

const res = await fetch(`${ENDPOINT}?query=${encodeURIComponent(QUERY)}`, {
  headers: {
    Accept: 'application/sparql-results+json',
    'User-Agent': 'sigma-municipality-loader/1.0 (https://github.com/midt-bg/sigma)',
  },
  signal: AbortSignal.timeout(60_000),
});
if (!res.ok) throw new Error(`Wikidata SPARQL failed: ${res.status} ${res.statusText}`);
const { results } = await res.json();

// Fold the rows (one per item × eik × region) into one record per item QID.
const byItem = new Map();
for (const b of results.bindings) {
  const qid = b.item.value.split('/').pop();
  if (!byItem.has(qid))
    byItem.set(qid, { qid, name: b.name?.value, eiks: new Set(), region: b.region?.value });
  const rec = byItem.get(qid);
  if (b.eik?.value) rec.eiks.add(b.eik.value);
  if (!rec.region && b.region?.value) rec.region = b.region.value;
}

const municipalities = [];
for (const rec of byItem.values()) {
  let eik;
  if (rec.eiks.size === 1) {
    eik = [...rec.eiks][0];
  } else if (rec.eiks.size > 1) {
    eik = RESOLVE_MULTI_EIK[rec.qid];
    if (!eik)
      throw new Error(
        `${rec.qid} (${rec.name}) has multiple P8894 values [${[...rec.eiks].join(', ')}] and no entry in RESOLVE_MULTI_EIK — resolve it against the БУЛСТАТ register, then add it.`,
      );
  } else {
    throw new Error(`${rec.qid} (${rec.name}) has no P8894 (ЕИК) on Wikidata`);
  }
  if (!/^[0-9]{9}$/.test(eik))
    throw new Error(`${rec.qid} (${rec.name}) ЕИК ${eik} is not a 9-digit code`);
  if (!rec.name) throw new Error(`${rec.qid} has no Bulgarian label`);
  if (!rec.region) throw new Error(`${rec.qid} (${rec.name}) has no region (P131)`);
  municipalities.push({ eik, name: rec.name, region: rec.region });
}

municipalities.sort((a, b) => a.eik.localeCompare(b.eik));

// Hard invariants — a regeneration that does not reproduce the known shape of the country is a bug.
const eiks = new Set(municipalities.map((m) => m.eik));
if (eiks.size !== municipalities.length) throw new Error('duplicate ЕИК in result set');
if (municipalities.length !== EXPECTED_COUNT)
  throw new Error(`expected ${EXPECTED_COUNT} municipalities, got ${municipalities.length}`);
const regions = new Set(municipalities.map((m) => m.region));
if (regions.size !== EXPECTED_REGIONS)
  throw new Error(
    `expected ${EXPECTED_REGIONS} regions, got ${regions.size}: ${[...regions].sort().join(', ')}`,
  );

const esc = (s) => s.replace(/'/g, "''");
const values = municipalities
  .map((m) => `  ('${m.eik}', '${esc(m.name)}', '${esc(m.region)}')`)
  .join(',\n');

const body = `-- Sigma — canonical registry of Bulgaria's ${EXPECTED_COUNT} municipalities (общини), keyed by ЕИК.
-- Generated by scripts/build-municipalities.mjs from Wikidata (P31 = Q1906268 "municipality of
-- Bulgaria", P8894 = ЕИК/БУЛСТАТ, P131 = region). Do NOT edit by hand — rerun the generator.
-- Loaded before normalize-raw.sql / refresh-slice.sql; an authority is bucketed 'община' iff its ЕИК is here.
CREATE TABLE IF NOT EXISTS municipality_eik (
  eik TEXT PRIMARY KEY,
  canonical_name TEXT NOT NULL,
  region TEXT
);

DELETE FROM municipality_eik;

INSERT INTO municipality_eik (eik, canonical_name, region) VALUES
${values};
`;

writeFileSync(OUT, body);
console.log(
  `wrote ${OUT}: ${municipalities.length} municipalities, ${regions.size} regions, ${body.length} bytes`,
);
