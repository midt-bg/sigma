#!/usr/bin/env node
// One-time generator for the Bulgaria район (NUTS2) map geometry used by /map's "По райони" mode.
// Source: GISCO NUTS 2021, 20M resolution, EPSG:4326 (© EuroGeographics). Filters the 6 BG NUTS2
// features and projects them using the bbox of the 28 NUTS3 features (not their own bbox) so both
// layers share the exact same coordinate space / viewBox as apps/web/app/lib/bg-region-geometry.ts
// and the район outlines line up pixel-perfect with the oblast map. Output:
// apps/web/app/lib/bg-macroregion-geometry.ts (committed; no runtime dep).
//
// Run: node scripts/build-geo-nuts2.mjs   (then `pnpm exec prettier --write apps/web/app/lib/bg-macroregion-geometry.ts`)

import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { computeBbox, makeProjector } from './geo-projection.mjs';

const SRC3 =
  'https://gisco-services.ec.europa.eu/distribution/v2/nuts/geojson/NUTS_RG_20M_2021_4326_LEVL_3.geojson';
const SRC2 =
  'https://gisco-services.ec.europa.eu/distribution/v2/nuts/geojson/NUTS_RG_20M_2021_4326_LEVL_2.geojson';
const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = resolve(root, 'apps/web/app/lib/bg-macroregion-geometry.ts');

const [res3, res2] = await Promise.all([
  fetch(SRC3, { signal: AbortSignal.timeout(30_000) }),
  fetch(SRC2, { signal: AbortSignal.timeout(30_000) }),
]);
if (!res3.ok) throw new Error(`GISCO fetch failed (LEVL_3): ${res3.status}`);
if (!res2.ok) throw new Error(`GISCO fetch failed (LEVL_2): ${res2.status}`);
const [gj3, gj2] = await Promise.all([res3.json(), res2.json()]);

const bg3 = gj3.features.filter((f) => String(f.properties.NUTS_ID || '').startsWith('BG'));
if (bg3.length !== 28) throw new Error(`expected 28 BG NUTS3 features, got ${bg3.length}`);

const bg2 = gj2.features.filter((f) => {
  const id = String(f.properties.NUTS_ID || '');
  return id.startsWith('BG') && id.length === 4;
});
if (bg2.length !== 6) throw new Error(`expected 6 BG NUTS2 features, got ${bg2.length}`);

// Shared coordinate space: project using the NUTS3 bbox so the район outlines align with the oblast
// map's viewBox, not their own (slightly different) bbox.
const { W, H, pathD } = makeProjector(computeBbox(bg3));

const regions = bg2
  .map((f) => ({ nuts2: f.properties.NUTS_ID, d: pathD(f.geometry) }))
  .sort((a, b) => a.nuts2.localeCompare(b.nuts2));

const body = `// Generated by scripts/build-geo-nuts2.mjs from GISCO NUTS 2021, 20M (© EuroGeographics,
// https://ec.europa.eu/eurostat/web/gisco). Do not edit by hand; rerun the script to regenerate.
// One SVG path per Bulgarian район, keyed by NUTS2 id (joins @sigma/config BG_REGIONS' nuts2 field).
// Projected in the same coordinate space as bg-region-geometry.ts (viewBox derived from the 28 NUTS3
// features), so the two layers align pixel-perfect when swapped in the /map choropleth.

export interface MacroRegionShape {
  nuts2: string;
  d: string;
}

export const BG_MACRO_MAP: { viewBox: string; regions: MacroRegionShape[] } = {
  viewBox: '0 0 ${W} ${H}',
  regions: ${JSON.stringify(regions)},
};
`;

writeFileSync(OUT, body);
console.log(`wrote ${OUT}: ${regions.length} regions, viewBox 0 0 ${W} ${H}, ${body.length} bytes`);
